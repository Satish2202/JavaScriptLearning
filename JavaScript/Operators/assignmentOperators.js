
/* Assign */

let x=10;
x=10+15;
console.log(x);

/* Add and Assign */
let y=5;
y += 25;   // y= y+25
console.log(y);

/* Subtract and Assign */
let a=5;
a -= 25;   // a= a-25
console.log(a);
/* Multiply and Assign */
let b=5;
b *= 25;   // b= b*25
console.log(b);
/* Division and Assign */
let c=5;
c /= 25;   // c= c/25
console.log(c);
/* Modulus and Assign */
let d=5;
d %= 25;   // d= d%25
console.log(d);

/* Exponential and Assign */
let e=5;
e **= 25;   // e= e%25
console.log(e);